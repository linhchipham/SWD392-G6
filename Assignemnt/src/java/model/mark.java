package model;

/**
 *
 * @author PC
 */
public class mark {
    double ass1;
    double ass2;
    double total1;
    double pro1;
    double pro2;
    double total2;
    double fin;
    double total;

    public mark(double ass1, double ass2, double total1, double pro1, double pro2, double total2, double fin, double total) {
        this.ass1 = ass1;
        this.ass2 = ass2;
        this.total1 = total1;
        this.pro1 = pro1;
        this.pro2 = pro2;
        this.total2 = total2;
        this.fin = fin;
        this.total = total;
    }

    public double getAss1() {
        return ass1;
    }

    public void setAss1(double ass1) {
        this.ass1 = ass1;
    }

    public double getAss2() {
        return ass2;
    }

    public void setAss2(double ass2) {
        this.ass2 = ass2;
    }

    public double getTotal1() {
        return total1;
    }

    public void setTotal1(double total1) {
        this.total1 = total1;
    }

    public double getPro1() {
        return pro1;
    }

    public void setPro1(double pro1) {
        this.pro1 = pro1;
    }

    public double getPro2() {
        return pro2;
    }

    public void setPro2(double pro2) {
        this.pro2 = pro2;
    }

    public double getTotal2() {
        return total2;
    }

    public void setTotal2(double total2) {
        this.total2 = total2;
    }

    public double getFin() {
        return fin;
    }

    public void setFin(double fin) {
        this.fin = fin;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }

  
    
}
