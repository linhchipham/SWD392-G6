package model;

/**
 *
 * @author PC
 */
public class User {
    String user;
    String pass;
    Role role;

    public Role getRole() {
        return role;
    }

    public void setRole(Role role) {
        this.role = role;
    }

    public User() {
    }

    public User(String user, String pass, Role role) {
        this.user = user;
        this.pass = pass;
        this.role = role;
    }
    
    public User(String user, String pass) {
        this.user = user;
        this.pass = pass;
    }


    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
    
            
}
